#include <stdlib.h>
#include "calc.h"

//function to add two intger and return result//
int op_add(int a, int b)
{
	return (a + b);
}

//function to subtraction two intgers and return result//
int op_sub(int a, int b)
{
	return (a - b);
}

//function to multiplication two intgers and return result//
int op_mul(int a, int b)
{
	return (a * b);
}

//function to division two intgers and return result//
int op_div(int a, int b)
{
	return (a / b);
}

//function to division two intgers and return reminder result//
int op_mod(int a, int b)
{
	return (a % b);
}
